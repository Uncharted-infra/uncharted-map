/** All country display names for search/highlight matching */
export const COUNTRY_NAMES = Object.values(COUNTRY_ISO_TO_NAME);

/**
 * Find a country name that matches the input (case-insensitive).
 * Prefers exact match, then prefix match.
 */
export function findMatchingCountry(input: string): string | null {
  const trimmed = input.trim();
  if (!trimmed) return null;

  const lower = trimmed.toLowerCase();
  const exact = COUNTRY_NAMES.find((c) => c.toLowerCase() === lower);
  if (exact) return exact;

  const prefix = COUNTRY_NAMES.find((c) => c.toLowerCase().startsWith(lower));
  return prefix ?? null;
}

/**
 * Get all country names that start with the input (case-insensitive).
 * Returns matches sorted alphabetically.
 */
export function getMatchingCountries(input: string): string[] {
  const trimmed = input.trim();
  if (!trimmed) return [];

  const lower = trimmed.toLowerCase();
  return COUNTRY_NAMES.filter((c) => c.toLowerCase().startsWith(lower)).sort((a, b) =>
    a.localeCompare(b)
  );
}
